/* --- Language Switcher --- */
let currentLang = localStorage.getItem('kin-thai-lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('kin-thai-lang', lang);
    document.documentElement.lang = lang;
    document.body.setAttribute('data-lang', lang);

    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = translations[lang]?.[key];
        if (translation) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                el.innerHTML = translation;
            }
        }
    });

    // Update toggle button
    const langEn = document.querySelectorAll('.lang-en');
    const langTh = document.querySelectorAll('.lang-th');
    langEn.forEach(el => el.style.display = lang === 'en' ? 'none' : '');
    langTh.forEach(el => el.style.display = lang === 'th' ? 'none' : '');
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);

    // Toggle listeners
    document.querySelectorAll('#lang-toggle, .lang-toggle').forEach(btn => {
        btn.addEventListener('click', () => {
            const newLang = currentLang === 'en' ? 'th' : 'en';
            setLanguage(newLang);
        });
    });
});
